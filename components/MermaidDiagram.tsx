"use client";
import { useEffect, useRef } from "react";

interface MermaidDiagramProps {
  chart: string;
  id: string;
}

export default function MermaidDiagram({ chart, id }: MermaidDiagramProps) {
  const ref = useRef<HTMLDivElement>(null);

  useEffect(() => {
    let cancelled = false;
    async function render() {
      const mermaid = (await import("mermaid")).default;
      mermaid.initialize({
        startOnLoad: false,
        // "base" theme gives full control over themeVariables without dark-theme CSS
        // overrides that silently kill node label contrast.
        theme: "base",
        themeVariables: {
          // --- canvas & backgrounds ---
          background: "#0F1726",
          mainBkg: "#16213A",           // default node fill (dark navy)
          nodeBorder: "#2DD4BF",        // teal brand border
          clusterBkg: "#0F1726",        // subgraph background
          clusterBorder: "#2DD4BF",

          // --- node text — must be light on dark fill ---
          primaryColor: "#16213A",      // default node fill (same as mainBkg)
          primaryBorderColor: "#2DD4BF",
          primaryTextColor: "#E9EEF6",  // light text on dark nodes
          nodeTextColor: "#E9EEF6",     // explicit override (Mermaid v11)

          // --- secondary nodes (rhombuses / decision boxes) ---
          secondaryColor: "#1a2d4a",
          secondaryBorderColor: "#F0C453",
          secondaryTextColor: "#E9EEF6",

          // --- tertiary nodes (database cylinders etc.) ---
          tertiaryColor: "#0A1628",
          tertiaryBorderColor: "#2DD4BF",
          tertiaryTextColor: "#E9EEF6",

          // --- edges & labels ---
          lineColor: "#A2B3C9",
          edgeLabelBackground: "#0F1726",  // avoid transparent bg on arrow labels
          labelTextColor: "#E9EEF6",

          // --- cluster/subgraph labels ---
          titleColor: "#2DD4BF",

          // --- text sizes ---
          fontSize: "15px",
        },
      });
      if (cancelled || !ref.current) return;
      try {
        const { svg } = await mermaid.render(`mermaid-${id}`, chart);
        if (!cancelled && ref.current) {
          // The SVG is generated by mermaid from static, hardcoded diagram strings
          // in components-data.ts and architecture/page.tsx — there is no user input
          // anywhere in this pipeline.  DOMPurify was stripping <foreignObject>
          // elements (which Mermaid v11 uses for node labels), making all labels
          // invisible.  Since the source is 100% static trusted data, we inject the
          // mermaid SVG directly without sanitization.
          ref.current.innerHTML = svg;
        }
      } catch (e) {
        if (!cancelled && ref.current) {
          // Error message is plain text — set via textContent, never innerHTML.
          const pre = document.createElement("pre");
          pre.className = "text-xs text-red-400 p-4";
          pre.textContent = String(e);
          ref.current.replaceChildren(pre);
        }
      }
    }
    render();
    return () => {
      cancelled = true;
    };
  }, [chart, id]);

  return (
    <div className="mermaid-wrapper">
      <div ref={ref} className="flex justify-center" />
    </div>
  );
}
